# zoo.py

def print_animal_habitat(animal_index):
    # Використовуйте введене число як індекс тварини, щоб надрукувати її
    if 1 <= animal_index <= len(animals):
        print(animals[animal_index - 1])
    else:
        print("Invalid input. Please enter a number between 1 and", len(animals))

# Цикл програми
while True:
    # Запитайте номер потрібної тварини
    user_input = input("Please enter the number of the habitat you would like to view (or type 'exit' to end): > ")

    # Перевірка на виход
    if user_input.lower() == 'exit':
        print("See you later!")
        break

    try:
        # Спробуйте перетворити введене значення в ціле число
        user_input = int(user_input)
        print_animal_habitat(user_input)
    except ValueError:
        print("Invalid input. Please enter a number or type 'exit'.")

